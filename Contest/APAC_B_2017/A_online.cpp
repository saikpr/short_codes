using namespace std;

long long int count_p(string s)
{
	long long int ans = 0;
	stack<long long int> v;
	for(int i =0;i<s.length();i++){
	if(s[i]=='('){
	v.push(1);
		}
	else{
	int count = 0;

	 while(v.top()!=1){

	     v.pop();

	     count++;

	  }

	v.pop();

	ans += count*(count+1)/2;

	v.push(0);
	}
	}
	long long int n =v.size();
	ans += n*(n+1)/2;

	return ans;
} 


int main()
{
	int T;
	cin>>T;
	int L,R;
	int min_p =0;
	for(size_t i = 0 ; i<T;i++)
	{

		
		cin>>L>>R;
		min_p = min(L,R);
		string st;
		for(size_t j =0 ; j <min_p; j++)
		{
			st = st+string("()");
		}
		cout<<"Case #"<<i+1<<": "<<count_p(st)<<endl;



	}
}